/// <summary>
/// ZP coding context: a single byte whose bit 0 is the MPS (more probable symbol)
/// and bits 1..7 index into the probability/transition table.
/// The context is mutated by the encoder/decoder after each symbol.
///
/// In the DjVu reference implementation, contexts are stored as simple bytes
/// where even indices have MPS=0 and odd indices have MPS=1.
/// State transitions are driven by the up[]/dn[] arrays in the table.
/// </summary>
internal struct ZpContext {

  /// <summary>
  /// Combined state+MPS byte.
  /// Bit 0 = MPS (0 or 1). Bits 1..7 (i.e. value >> 1) = state index into ZpTables.
  /// Even values mean MPS=0, odd values mean MPS=1.
  /// </summary>
  public byte Value;

  public ZpContext() => Value = 0;

  public ZpContext(byte value) => Value = value;
}

